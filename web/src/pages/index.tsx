import type { GetStaticProps, NextPage } from "next";
import Head from "next/head";
import { useSession } from "../modules/auth/store/useSession";
import { AuthenticatedHomePage } from "../modules/home/AuthenticatedHomePage";
import { UnauthenticatedHomePage } from "../modules/home/UnauthenticatedHomePage";
import { getProducts } from "../modules/product/api/useProducts";
import { Product } from "../modules/product/product.model";

type HomeProps = { products: Product[] };

const Home: NextPage<HomeProps> = ({ products }) => {
  const status = useSession((state) => state.status);

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {status === "signIn" ? (
        <AuthenticatedHomePage products={products} />
      ) : (
        <UnauthenticatedHomePage products={products} />
      )}
    </>
  );
};

export const getStaticProps: GetStaticProps<HomeProps> = async () => {
  const products = await getProducts();

  return { props: { products } };
};

export default Home;
